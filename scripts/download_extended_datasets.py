def download_url(url: str, output_path: Path, zip_extract: str | None = None, expected_sha256: str | None = None) -> bool:
    """Download from URL with idempotent atomic writes.
    
    - Uses atomic writes (temp file + rename) for idempotency
    - Optionally verifies SHA256 checksum
    - Can extract specific file from zip archives
    """
    import io
    import zipfile
    
    try:
        resp = requests.get(url, timeout=120, stream=True)
        resp.raise_for_status()
        content = resp.content
        
        if zip_extract:
            # Extract specific file from zip
            with zipfile.ZipFile(io.BytesIO(content)) as zf:
                for name in zf.namelist():
                    if name.endswith(zip_extract):
                        content = zf.read(name)
                        break
                else:
                    print(f"  [ERROR] {zip_extract} not found in zip")
                    return False
        
        # Verify checksum if provided
        if expected_sha256:
            actual_sha256 = hashlib.sha256(content).hexdigest()
            if actual_sha256 != expected_sha256:
                print(f"  [ERROR] SHA256 mismatch: expected {expected_sha256[:16]}... got {actual_sha256[:16]}...")
                return False
        
        # Atomic write: temp file + rename
        tmp_path = output_path.with_suffix(output_path.suffix + '.tmp')
        try:
            tmp_path.write_bytes(content)
            tmp_path.rename(output_path)  # Atomic on POSIX
            size_kb = len(content) / 1024
            if zip_extract:
                print(f"  [OK] Extracted {zip_extract} ({size_kb:.1f} KB)")
            else:
                print(f"  [OK] Downloaded {size_kb:.1f} KB")
            return True
        except Exception:
            tmp_path.unlink(missing_ok=True)
            raise
            
    except requests.exceptions.Timeout:
        print(f"  [ERROR] Timeout after 120s")
        return False
    except requests.exceptions.HTTPError as e:
        print(f"  [ERROR] HTTP {e.response.status_code}: {url[:60]}...")
        return False
    except Exception as e:
        print(f"  [ERROR] {e}")
        return False


def download_hf_dataset(hf_id: str, config: str | None, output_path: Path, audio_decode: bool = False) -> bool:
    """Download from HuggingFace with idempotent atomic writes.
    
    Uses trust_remote_code=True to support datasets with loading scripts.
    Writes to temp file first, then atomic rename for idempotency.
    """
    try:
        from datasets import load_dataset
        import tempfile
        
        def process_example(ex: dict, split_name: str | None = None) -> dict:
            """Convert example, handling audio fields gracefully."""
            ex_dict = {}
            if split_name:
                ex_dict["_split"] = split_name
            for k, v in ex.items():
                if hasattr(v, 'get') and 'path' in v:  # Audio dict
                    ex_dict[k] = {"path": str(v.get("path", "")), "sampling_rate": v.get("sampling_rate")}
                elif hasattr(v, 'path'):  # Audio object
                    ex_dict[k] = {"path": str(getattr(v, 'path', '')), "sampling_rate": getattr(v, 'sampling_rate', None)}
                elif isinstance(v, (dict, list, str, int, float, bool, type(None))):
                    ex_dict[k] = v
                else:
                    ex_dict[k] = str(v)  # Fallback for complex types
            return ex_dict
        
        def atomic_write(data: list, path: Path) -> None:
            """Write data to file atomically (temp file + rename)."""
            # Write to temp file in same directory (for atomic rename)
            tmp_path = path.with_suffix(path.suffix + '.tmp')
            try:
                tmp_path.write_text(json.dumps(data, indent=2, ensure_ascii=False))
                tmp_path.rename(path)  # Atomic on POSIX
            except Exception:
                tmp_path.unlink(missing_ok=True)
                raise
        
        # Load kwargs - trust_remote_code removed as HF deprecated it
        # Datasets with loading scripts need alternative sources
        load_kwargs = {}
        
        # For audio datasets, try to load without decoding audio
        if audio_decode:
            print("  [INFO] Loading with audio decoding disabled (text annotations only)")
            try:
                ds = load_dataset(hf_id, config, **load_kwargs)
                # Remove audio columns to avoid decoding issues
                if hasattr(ds, 'keys'):
                    for split_name in ds.keys():
                        audio_cols = [c for c in ds[split_name].column_names if 'audio' in c.lower()]
                        if audio_cols:
                            ds[split_name] = ds[split_name].remove_columns(audio_cols)
                    all_data = []
                    for split_name in ds.keys():
                        for ex in ds[split_name]:
                            all_data.append(process_example(dict(ex), split_name))
                    atomic_write(all_data, output_path)
                    print(f"  [OK] Downloaded {len(all_data)} examples from {len(list(ds.keys()))} splits (text only)")
                    return True
            except Exception as e:
                print(f"  [DEBUG] Audio dataset load failed: {e}")
                return False
        
        # Try standard splits first (prefer test for evaluation)
        for split in ["test", "validation", "train"]:
            try:
                ds = load_dataset(hf_id, config, split=split, **load_kwargs)
                data = [process_example(dict(ex)) for ex in ds]
                atomic_write(data, output_path)
                print(f"  [OK] Downloaded {len(data)} examples from {split} split")
                return True
            except (ValueError, KeyError):
                continue
            except Exception as e:
                # Check for config-related errors
                if "Config name is missing" in str(e) or "Please pick one" in str(e):
                    print(f"  [WARN] Dataset requires config. Available: see error below")
                    raise
                continue
        
        # Try loading entire dataset (for datasets with non-standard splits)
        try:
            ds = load_dataset(hf_id, config, **load_kwargs)
            if hasattr(ds, 'keys'):
                all_data = []
                for split_name in ds.keys():
                    for ex in ds[split_name]:
                        all_data.append(process_example(dict(ex), split_name))
                atomic_write(all_data, output_path)
                print(f"  [OK] Downloaded {len(all_data)} examples from {len(list(ds.keys()))} splits")
                return True
        except Exception as e:
            print(f"  [DEBUG] Full load failed: {e}")
        
        print(f"  [ERROR] No valid split found")
        return False
    except Exception as e:
        print(f"  [ERROR] HuggingFace download failed: {e}")
        return False


def download_dataset(name: str, config: dict) -> bool:
    """Download a single dataset."""
    output_path = CACHE_DIR / config["output"]
    
    if output_path.exists():
        print(f"  [SKIP] {name}: already exists at {output_path}")
        return True
    
    print(f"  [DOWNLOAD] {name}: {config.get('description', '')}")
    
    # URL download
    if "url" in config and not config.get("manual"):
        zip_extract = config.get("zip_extract")
        return download_url(config["url"], output_path, zip_extract=zip_extract)
    
    # HuggingFace dataset
    if "hf_dataset" in config:
        return download_hf_dataset(
            config["hf_dataset"],
            config.get("config"),
            output_path,
            audio_decode=config.get("audio_decode", False),
        )
    
    # Manual download required
    if config.get("manual"):
        print(f"  [MANUAL] Download from: {config.get('url', 'see documentation')}")
        return False
    
    print(f"  [ERROR] No download method configured")
    return False


def validate_dataset_content(filepath: Path) -> dict:
    """Perform deeper validation on dataset content."""
    result = {"valid": True, "format": None, "records": 0, "errors": []}
    
    try:
        content = filepath.read_text(errors="replace")
        suffix = filepath.suffix.lower()
        
        if suffix == ".json":
            # Try parsing as regular JSON first
            try:
                data = json.loads(content)
                result["format"] = "json"
                if isinstance(data, list):
                    result["records"] = len(data)
                elif isinstance(data, dict):
                    # STIX format (MITRE ATT&CK)
                    if "objects" in data:
                        result["records"] = len(data["objects"])
                        result["format"] = "stix"
                    else:
                        result["records"] = 1
            except json.JSONDecodeError:
                # Try JSONL (HuggingFace NER JSON format is actually JSONL)
                lines = [l for l in content.strip().split("\n") if l]
                if lines:
                    try:
                        json.loads(lines[0])  # Test first line
                        result["format"] = "jsonl"
                        result["records"] = len(lines)
                    except json.JSONDecodeError as e:
                        result["valid"] = False
                        result["errors"].append(f"Invalid JSON: {e}")
        
        elif suffix == ".jsonl":
            lines = [l for l in content.strip().split("\n") if l]
            result["format"] = "jsonl"
            result["records"] = len(lines)
            # Validate each line is valid JSON
            for i, line in enumerate(lines[:5]):  # Check first 5
                try:
                    json.loads(line)
                except json.JSONDecodeError as e:
                    result["errors"].append(f"Invalid JSON on line {i+1}: {e}")
                    result["valid"] = False
        
        elif suffix in [".conll", ".conllu", ".bio"]:
            # Count sentences (blank-line separated)
            result["format"] = "conll"
            sentences = content.split("\n\n")
            result["records"] = len([s for s in sentences if s.strip()])
        
        elif suffix == ".tsv":
            result["format"] = "tsv"
            lines = content.strip().split("\n")
            result["records"] = len(lines)
        
        elif suffix == ".csv":
            result["format"] = "csv"
            lines = content.strip().split("\n")
            result["records"] = len(lines) - 1  # Exclude header
        
        elif suffix == ".txt":
            result["format"] = "txt"
            lines = content.strip().split("\n")
            result["records"] = len(lines)
        
        elif suffix in [".xml"]:
            result["format"] = "xml"
            result["records"] = content.count("<document") or content.count("<doc")
        
        elif suffix in [".zip", ".tar", ".gz"]:
            result["format"] = "archive"
            result["records"] = filepath.stat().st_size  # Just size for archives
        
        else:
            result["format"] = "unknown"
            result["records"] = 0
            
    except Exception as e:
        result["valid"] = False
        result["errors"].append(str(e))
    
    return result


def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("--all", action="store_true", help="Download all datasets")
    parser.add_argument("--group", type=str, help="Download specific group")
    parser.add_argument("--dataset", type=str, help="Download specific dataset")
    parser.add_argument("--list", action="store_true", help="List available datasets")
    parser.add_argument("--validate", action="store_true", help="Validate downloaded datasets")
    parser.add_argument("--deep-validate", action="store_true", help="Deep validation with content parsing")
    parser.add_argument("--compute-hashes", action="store_true", help="Compute SHA256 hashes for all cached files")
    parser.add_argument("--from-rust", action="store_true", help="Download only datasets with HF IDs from Rust registry")
    args = parser.parse_args()
    
    # Deep validation mode
    if args.deep_validate:
        print(f"Deep validating datasets in {CACHE_DIR}...")
        valid_count = 0
        invalid_count = 0
        for filepath in sorted(CACHE_DIR.glob("*")):
            if filepath.is_file():
                result = validate_dataset_content(filepath)
                sha = compute_sha256(filepath)
                size_kb = filepath.stat().st_size / 1024
                status = "✓" if result["valid"] else "✗"
                print(f"  {status} {filepath.name}: {size_kb:.1f}KB, format={result['format']}, records={result['records']}, sha256={sha[:16]}...")
                if result["errors"]:
                    for err in result["errors"]:
                        print(f"      ERROR: {err}")
                if result["valid"]:
                    valid_count += 1
                else:
                    invalid_count += 1
        print(f"\nSummary: {valid_count} valid, {invalid_count} invalid")
        return
    
    # Validation mode
    if args.validate or args.compute_hashes:
        print(f"Scanning {CACHE_DIR}...")
        for filepath in sorted(CACHE_DIR.glob("*")):
            if filepath.is_file():
                sha = compute_sha256(filepath)
                size_kb = filepath.stat().st_size / 1024
                print(f"  {filepath.name}: {size_kb:.1f}KB, sha256={sha[:16]}...")
        return
    
    if args.list:
        groups = {}
        for name, cfg in DATASETS.items():
            g = cfg.get("group", "other")
            if g not in groups:
                groups[g] = []
            groups[g].append((name, cfg.get("description", "")))
        
        for group, datasets in sorted(groups.items()):
            print(f"\n{group}:")
            for name, desc in datasets:
                print(f"  {name}: {desc}")
        return
    
    if args.from_rust:
        if not DATASETS_FROM_REGISTRY:
            print("No datasets from Rust registry. Run: cargo test -p anno --features eval generate_python_download_config -- --ignored")
            return
        items = [(n, c) for n, c in DATASETS.items() if n in DATASETS_FROM_REGISTRY]
        print(f"Downloading {len(items)} datasets from Rust registry")
    elif args.dataset:
        if args.dataset not in DATASETS:
            print(f"Unknown dataset: {args.dataset}")
            return
        items = [(args.dataset, DATASETS[args.dataset])]
    elif args.group:
        items = [(n, c) for n, c in DATASETS.items() if c.get("group") == args.group]
        if not items:
            print(f"No datasets in group: {args.group}")
            return
    elif args.all:
        items = list(DATASETS.items())
    else:
        print("Use --all, --group GROUP, --dataset NAME, or --from-rust")
        return
    
    ok, fail, manual = 0, 0, 0
    for name, cfg in items:
        print(f"\n=== {name} ===")
        if cfg.get("manual"):
            manual += 1
            download_dataset(name, cfg)
        elif download_dataset(name, cfg):
            ok += 1
        else:
            fail += 1
    
    print(f"\n\nDone: {ok} downloaded, {manual} manual, {fail} failed")


if __name__ == "__main__":
    main()
