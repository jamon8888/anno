def compute_sha256(filepath: Path) -> str:
    """Compute SHA256 hash of a file."""
    sha256_hash = hashlib.sha256()
    with open(filepath, "rb") as f:
        for chunk in iter(lambda: f.read(4096), b""):
            sha256_hash.update(chunk)
    return sha256_hash.hexdigest()


def validate_download(filepath: Path, expected_sha256: str | None = None, expected_docs: int | None = None) -> dict:
    """Validate downloaded file with SHA256 and basic content checks."""
    result = {"valid": True, "sha256": None, "docs": None, "errors": []}
    
    if not filepath.exists():
        result["valid"] = False
        result["errors"].append("File not found")
        return result
    
    # Compute SHA256
    actual_sha256 = compute_sha256(filepath)
    result["sha256"] = actual_sha256
    
    if expected_sha256 and actual_sha256 != expected_sha256:
        result["valid"] = False
        result["errors"].append(f"SHA256 mismatch: expected {expected_sha256[:16]}... got {actual_sha256[:16]}...")
    
    # Count documents/lines for basic validation
    try:
        content = filepath.read_text(errors="replace")
        if filepath.suffix == ".json":
            data = json.loads(content)
            result["docs"] = len(data) if isinstance(data, list) else 1
        elif filepath.suffix in [".conll", ".conllu", ".txt", ".tsv"]:
            # Count blank-line separated documents
            result["docs"] = content.count("\n\n") + 1
        elif filepath.suffix == ".jsonl":
            result["docs"] = len([l for l in content.strip().split("\n") if l])
    except Exception as e:
        result["errors"].append(f"Content check failed: {e}")
    
    if expected_docs and result["docs"] and abs(result["docs"] - expected_docs) > expected_docs * 0.1:
        result["errors"].append(f"Doc count mismatch: expected ~{expected_docs}, got {result['docs']}")
    
    return result

